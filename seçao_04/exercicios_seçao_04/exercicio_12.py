"""
Leia uma distância em milhas e apresente-a convertida em quilômetros. A fórmula de
conversão é: K = 1.61 * M, sendo K a distância em quilômetros e M em milhas.
"""

print("Vamos converter MILHAS em QUILÔMETROS.")

milha = float(input("Digite a quantidade de milhas: "))

kilometros = 1.61 * milha

print(f"A distância em quilômetros é de {kilometros}km.")



"""
Leia uma velocidade em m/s e apresente-a convetida em km/h. A fórmula de conversão é:
K = M * 3.6, sendo K a velocidade em km/h e M em m/s.
"""

print("Vamos converter uma VELOCIDADE de m/s para km/s.")

metros_por_segundo = float(input("Digite a velocidade em m/s: "))

kilometros_por_hora = metros_por_segundo * 3.6
print(f"A velocidade em km/h será de {kilometros_por_hora}km/h")





"""
Leia um valor de área em hectares e apresente-o convertido em metros quadrados m².
A fórmula de conversão é: M = H * 10000, sendo M a área em metros quadrados e H
a área em hectares.
"""

print()
print("Vamos converter a área de HECTARES para METROS QUADRADOS.")

hectares = float(input("Digite a área em HECTARES: "))

metro_quadrado = hectares * 10000

print(f"O valor da área em METROS QUADRADOS será de {metro_quadrado}m².")




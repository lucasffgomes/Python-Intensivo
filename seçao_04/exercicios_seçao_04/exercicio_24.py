"""
Leia um valor de área em metros quadrados m² e apresente-o convertido em acres. A
fórmula de conversão é: A = M * 0.000247, sendo M a área em metros quadrados e A a área
em acres.
"""

print()
print("Vamos converter a área de M² para ACRES.")

metro_quadrado = float(input("Digite o valor do METRO QUADRADO: "))

acre = metro_quadrado * 0.000247

print(f"O valor da área em ACRES é de {acre}")




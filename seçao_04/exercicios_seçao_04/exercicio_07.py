"""
Leia uma temperatura em Graus Fahrenheit a apresente-a convertidaem graus Celsius.
A fórmula de conversão é: C = 5.0 * (F - 32.0) / 9.0, sendo C a temperatura em
Celsius e F a temperatura em Fahrenheit.
"""

print("Vamos descobrir a temperatura em Fahrenheit.")

fahrenheit = float(input("Digite a temperatura em Fahrenheit: "))

celsius = 5 * (fahrenheit - 32) / 9

print("A temperatura em Celsius é", celsius, "ºC")






